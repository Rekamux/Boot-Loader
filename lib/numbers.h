/*********************
 * NUMBERS
 *
 * Utilities to manipulate numbers
 ********************/
#ifndef NUMBERS_H
#define NUMBERS_H

/**
 * Displays a digit into a char
 */
char dtoc(unsigned short);

/**
 * Displays a number written in hexadecimal
 */
void itohex(unsigned long);

/**
 * Displays a number written in 8 hexadecimals
 */
void itohex8(unsigned long);

/**
 * Displays a number written in decimal
 */
void itodec(long);

#endif
